# ---------------------------------------------------------------------------
# Runner: проверяет normalizer против калибровочного набора
# ---------------------------------------------------------------------------

def run_calibration():
    import sys
    sys.path.insert(0, '.')
    from text_normalizer import normalize_text

    total = len(CALIBRATION_SET)
    passed = 0
    failed = 0
    todo = 0

    print("=" * 70)
    print("Normalizer Calibration Check")
    print("=" * 70)

    for orig, expected in CALIBRATION_SET:
        actual = normalize_text(orig, fix_slang=True, fix_repeats=True, fix_spelling=False)

        if expected is None:
            # No normalization expected — check nothing changed
            # (or: user hasn't filled in expected yet)
            if actual == orig:
                passed += 1
            else:
                print(f"\n  [?] Нормализатор изменил, но expected=None:")
                print(f"      ORIG: {orig[:80]}")
                print(f"      GOT:  {actual[:80]}")
                print(f"      → Проверь: это правильная замена? Если да, обнови expected.")
                todo += 1
        else:
            if actual == expected:
                passed += 1
            else:
                failed += 1
                print(f"\n  [FAIL] {orig[:60]}")
                print(f"      EXPECTED: {expected[:80]}")
                print(f"      GOT:      {actual[:80]}")

    print(f"\n{'=' * 70}")
    print(f"  Total:  {total}")
    print(f"  Passed: {passed}")
    print(f"  Failed: {failed}")
    print(f"  Todo:   {todo} (expected=None but normalizer changed something)")
    print(f"{'=' * 70}")

    # Also check KNOWN_MISSPELLINGS coverage
    print(f"\n  Known misspellings in dict: {len(KNOWN_MISSPELLINGS)}")
    print(f"  Missing slang candidates:   {len([v for v in MISSING_SLANG.values() if v])}")


if __name__ == "__main__":
    run_calibration()
